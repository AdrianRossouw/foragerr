# --- truthfulness scans (FRG-SITE-004/006) -----------------------------------

ABSENCE_SECTION_RE = re.compile(r'<section[^>]*\bdata-absence\b.*?</section>',
                                re.S)


def scan_output(pages: dict, banned: list, license_name: str, repo_url: str):
    for name, content in pages.items():
        # The one dedicated absence section may name missing artifacts to state
        # they do not exist (FRG-SITE-004); positive claims elsewhere still fail.
        if len(ABSENCE_SECTION_RE.findall(content)) > 1:
            fail(f'more than one absence section in {name}')
        low = ABSENCE_SECTION_RE.sub('', content).lower()
        for phrase in banned:
            if phrase.lower() in low:
                fail(f'banned phrase {phrase!r} found in {name}')
    if license_name not in pages['index.html']:
        fail('footer license claim missing from built output')
    for name, content in pages.items():
        if repo_url not in content:
            fail(f'repository link missing from {name}')


# --- main --------------------------------------------------------------------

def build(root: Path, out: Path) -> None:
    cfg = load_config()
    registry = load_registry(root)
    matrix = load_matrix(root)
    releases = load_changelog(root)
    tags = git_tags(root)
    risks = load_risks(root)
    license_name = license_family(root)
    banned = load_banned()

    untagged = [r['version'] for r in releases if r['version'] not in tags]
    if untagged:
        fail(f'CHANGELOG releases without a git tag: {", ".join(untagged)}')

    pages = render_pages(root, cfg, registry, matrix, releases, risks,
                         license_name)
    scan_output(pages, banned, license_name, cfg['site']['repo_url'])

    # Everything validated — only now touch the output directory.
    if out.exists():
        shutil.rmtree(out)
    (out / 'static').mkdir(parents=True)
    (out / 'assets' / 'shots').mkdir(parents=True)
    for name, content in pages.items():
        (out / name).write_text(content, encoding='utf-8')
    shutil.copy(SITE_DIR / 'static' / 'site.css', out / 'static' / 'site.css')
    shutil.copy(root / 'docs/assets/foragerr-mark.svg',
                out / 'assets' / 'foragerr-mark.svg')
    for fname, _ in PRODUCT_SHOTS:
        shutil.copy(root / 'docs/readme-assets' / fname,
                    out / 'assets' / 'shots' / fname)
    print(f'site built: {len(pages)} pages, {len(releases)} releases, '
          f'{len(registry)} requirements → {out}')


def main() -> None:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument('--root', type=Path,
                    default=SITE_DIR.parent, help='repository root')
    ap.add_argument('--out', type=Path, default=None,
                    help='output directory (default <root>/_site)')
    args = ap.parse_args()
    root = args.root.resolve()
    build(root, (args.out or root / '_site').resolve())


if __name__ == '__main__':
    main()
