        return (f'<div class="card release"><div class="rel-head">'
                f'<span class="rel-version">{esc(r["version"])}</span>'
                f'<span class="rel-date">{esc(r["date"])}</span></div>'
                f'<div class="rel-body">{intro}{sections}{idrow}</div></div>')

