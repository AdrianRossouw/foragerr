PAGES = [('index.html', 'Overview'), ('method.html', 'The Method'),
         ('timeline.html', 'Timeline'), ('trust.html', 'Trust Center'),
         ('product.html', 'Product')]


def template(name: str) -> Template:
    return Template(read_text(SITE_DIR / 'templates' / name, f'template {name}'))


def nav_html(active: str) -> str:
    out = []
    for href, label in PAGES:
        cls = 'nav-link active' if href == active else 'nav-link'
        out.append(f'<a class="{cls}" href="{href}">{esc(label)}</a>')
    return ''.join(out)


def chip(text: str, tone: str = 'neutral') -> str:
    return f'<span class="chip chip-{tone}">{esc(text)}</span>'


def render_pages(root, cfg, registry, matrix, releases, risks, license_name):
    site = cfg['site']
    traced, live = coverage(registry, matrix)
    ex = exemplar_card(cfg, registry, matrix)
    artifacts = trust_artifacts(root, cfg, registry, risks, releases)
    governance = governance_artifacts(root, cfg)
    cov = coverage_breakdown(registry, matrix)

    base = template('base.html')
    mark_svg = read_text(root / 'docs/assets/foragerr-mark.svg', 'logo mark')

    def page(active, title, content):
        return base.substitute(
            title=esc(title), description=esc(site['description']),
            nav=nav_html(active), repo_url=esc(site['repo_url']),
            mark=mark_svg, license=esc(license_name), content=content)

    stats = [
        (str(live), 'requirements in the registry'),
        (f'{cov["implemented_tested"]} of {cov["implemented_total"]}',
         'implemented requirements have tagged tests'),
        (str(len(releases)), 'tagged releases'),
        (str(len(risks)), 'risks tracked in the register'),
    ]
    stat_tiles = ''.join(
        f'<div class="stat"><div class="stat-v">{esc(v)}</div>'
        f'<div class="stat-l">{esc(l)}</div></div>' for v, l in stats)

    pillars = [
        ('01', 'A requirement registry',
         'Every behavior foragerr is meant to have is written down first, with '
         'a stable ID. IDs are allocated in the registry when a change is '
         'proposed, and a commit hook rejects any commit citing an ID that '
         'is not registered.',
         ['IDs allocated before code', 'Never reused or renumbered',
          'Enforced by the commit hook']),
        ('02', 'End-to-end traceability',
         'Each requirement links to the spec that defines it, the tagged tests '
         'that prove it, and the commits that built it. The matrix is '
         'regenerated from repository state — spec headings, test tags, commit '
         'trailers — and inconsistencies fail the run.',
         ['Requirement → spec → test → commit', 'Generated, not asserted',
          'Gaps are visible, not hidden']),
        ('03', 'Per-change review gates',
         'The owner approves every proposal before implementation begins, and '
         'no change merges until its gate passes: the full test suite green, '
         'traceability and dependency checks at zero, and a multi-angle review '
         'of the diff.',
         ['Owner approval before code', 'Full suite + consistency checks',
          'Multi-angle diff review']),
        ('04', 'Release records',
         'Every tagged release carries the same four artifacts: a CHANGELOG '
         'entry citing the requirements it moved, an annotated tag, the pushed '
         'history, and a GitHub Release. Tags are immutable — a mistake gets a '
         'new patch release, never a moved tag.',
         ['CHANGELOG + tag + GitHub Release', 'Requirement refs in every entry',
          'Tags are immutable']),
    ]

    def pillar_cards(compact: bool) -> str:
        out = []
        for n, title, body, points in pillars:
            pts = (''.join(f'<span class="pill">{esc(p)}</span>' for p in points)
                   if compact else
                   ''.join(f'<div class="pillar-point"><span>—</span>'
                           f'<span>{esc(p)}</span></div>' for p in points))
            out.append(
                f'<div class="card pillar"><div class="pillar-head">'
                f'<span class="pillar-n">{n}</span>'
                f'<span class="pillar-title">{esc(title)}</span></div>'
                f'<p>{esc(body)}</p><div class="pillar-points">{pts}</div></div>')
        return ''.join(out)

    trace_rows = ''.join(
        f'<div class="trace-row"><span class="trace-k">{esc(k)}</span>'
        f'<span class="trace-v">{esc(v)}</span></div>' for k, v in ex['rows'])

    overview = template('overview.html').substitute(
        exemplar_id=esc(ex['id']), exemplar_title=esc(ex['title']),
        trace_rows=trace_rows, stat_tiles=stat_tiles,
        pillar_cards=pillar_cards(compact=True),
        matrix_url=esc(f'{site["repo_url"]}/blob/main/docs/traceability/matrix.md'),
        n_releases=str(len(releases)))

    lifecycle = [
        ('01', 'Propose', 'An OpenSpec change defines the requirements; their '
                          'IDs are allocated in the registry.'),
        ('02', 'Approve', 'The owner approves every proposal before '
                          'implementation begins.'),
        ('03', 'Implement', 'Claude builds against the spec on a branch; '
                            'main stays green.'),
        ('04', 'Prove', 'Every requirement gets automated tests tagged with '
                        'its ID.'),
        ('05', 'Gate', 'Full test suite, traceability and dependency checks, '
                       'and a multi-angle review of the diff.'),
        ('06', 'Release', 'A CHANGELOG entry, an annotated tag, and a GitHub '
                          'Release — minted together.'),
    ]
    lifecycle_html = ''.join(
        f'<div class="life-step"><div class="pillar-n">{n}</div>'
        f'<div class="life-title">{esc(t)}</div><div class="life-body">'
        f'{esc(b)}</div></div>' for n, t, b in lifecycle)

    method = template('method.html').substitute(
        lifecycle=lifecycle_html, pillar_cards=pillar_cards(compact=False))

    groups, seen = [], {}
    for r in releases:
        minor = '.'.join(r['version'].split('.')[:2]) + '.x'
        if minor not in seen:
            seen[minor] = {'label': minor, 'entries': []}
            groups.append(seen[minor])
        seen[minor]['entries'].append(r)

    def release_entry(r):
        intro = ''.join(f'<p>{inline_md(p)}</p>' for p in r['intro'])
