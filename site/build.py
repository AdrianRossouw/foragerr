"""Build the foragerr public site (FRG-SITE-001..006).

Renders five static pages — Overview, The Method, Timeline, Trust Center,
Product — from committed repository artifacts only:

  docs/traceability/requirements-registry.md
  docs/traceability/matrix.md
  CHANGELOG.md
  docs/security/risk-register.md
  LICENSE
  git tags

Every displayed fact (count, status, version, date, ID, path, commit) comes
from these sources at build time; templates carry no hand-maintained factual
values (FRG-SITE-001). All sources are loaded and validated, and all pages are
rendered and scanned, before a single output file is written — a failing build
writes nothing.

Usage: python site/build.py [--root REPO] [--out DIR]
"""
