def fail(msg: str) -> 'NoReturn':  # noqa: F821 - py3.11 typing kept light
    print(f'site build failed: {msg}', file=sys.stderr)
    sys.exit(1)


def read_text(path: Path, what: str) -> str:
    if not path.is_file():
        fail(f'missing source artifact: {what} ({path})')
    return path.read_text(encoding='utf-8')


def esc(s: str) -> str:
    return html.escape(s, quote=True)


def inline_md(s: str) -> str:
    """Escape, then render the small inline-markdown subset the sources use."""
    s = esc(s)
    s = re.sub(r'`([^`]+)`', r'<code>\1</code>', s)
    s = re.sub(r'\*\*([^*]+)\*\*', r'<strong>\1</strong>', s)
    s = ID_RE.sub(lambda m: f'<span class="frg-id">{m.group(0)}</span>', s)
    return s


# --- source artifact loaders -------------------------------------------------

def load_config() -> dict:
    cfg = tomllib.loads(read_text(SITE_DIR / 'site.toml', 'site config'))
    for section, key in (('site', 'repo_url'), ('site', 'releases_url'),
                         ('site', 'title'), ('site', 'description'),
                         ('hero', 'exemplar_requirement')):
        if not cfg.get(section, {}).get(key):
            fail(f'site.toml missing [{section}] {key}')
    return cfg


def load_registry(root: Path) -> dict:
    rows = {}
    text = read_text(root / 'docs/traceability/requirements-registry.md',
                     'requirements registry')
    for line in text.splitlines():
        cells = [c.strip() for c in line.strip().strip('|').split('|')]
        if len(cells) >= 5 and ID_RE.fullmatch(cells[0]):
            rows[cells[0]] = {'title': cells[1], 'spec': cells[2],
                              'status': cells[3], 'milestone': cells[4]}
    if not rows:
        fail('requirements registry parsed to zero rows')
    return rows


def load_matrix(root: Path) -> dict:
    rows = {}
    text = read_text(root / 'docs/traceability/matrix.md', 'traceability matrix')
    for line in text.splitlines():
        cells = [c.strip() for c in line.strip().strip('|').split('|')]
        if len(cells) >= 6 and ID_RE.fullmatch(cells[0]):
            split = lambda v: [] if v == '—' else [p.strip() for p in v.split(',')]
            rows[cells[0]] = {'status': cells[1], 'milestone': cells[2],
                              'spec': cells[3], 'tests': split(cells[4]),
                              'commits': split(cells[5])}
    if not rows:
        fail('traceability matrix parsed to zero rows')
    return rows


def load_changelog(root: Path) -> list:
    """Parse CHANGELOG.md into releases: version, date, intro, sections, ids."""
    text = read_text(root / 'CHANGELOG.md', 'CHANGELOG')
    releases, current = [], None
    section = None
    for line in text.splitlines():
        m = RELEASE_RE.match(line)
        if m:
            current = {'version': m.group(1), 'date': m.group(2),
                       'intro': [], 'sections': []}
            releases.append(current)
            section = None
            continue
        if current is None:
            continue
        if line.startswith('### '):
            section = {'name': line[4:].strip(), 'items': []}
            current['sections'].append(section)
            continue
        bucket = section['items'] if section else current['intro']
        if line.startswith('- '):
            bucket.append(line[2:].strip())
        elif line.startswith('  ') and line.strip() and bucket:
            bucket[-1] += ' ' + line.strip()
        elif line.strip() and not section:
            current['intro'].append(line.strip())
        elif line.strip() and section:
            section['items'].append(line.strip())
    if not releases:
        fail('CHANGELOG parsed to zero releases')
    for r in releases:
        blob = ' '.join(r['intro']) + ' '.join(
            i for s in r['sections'] for i in s['items'])
        r['ids'] = sorted(set(ID_RE.findall(blob)))
    return releases


def git_tags(root: Path) -> set:
    out = subprocess.run(['git', 'tag'], capture_output=True, text=True, cwd=root)
    if out.returncode != 0:
        fail(f'git tag failed: {out.stderr.strip()}')
    tags = set(out.stdout.split())
    if not tags:
        fail('repository has no tags')
    return tags


def load_risks(root: Path) -> list:
    risks = []
    text = read_text(root / 'docs/security/risk-register.md', 'risk register')
    for line in text.splitlines():
        cells = [c.strip() for c in line.strip().strip('|').split('|')]
        if len(cells) >= 9 and re.fullmatch(r'RISK-\d{3}', cells[0]):
            risks.append({'id': cells[0], 'desc': cells[1], 'stride': cells[2],
                          'component': cells[3], 'l': cells[4], 'i': cells[5],
                          'status': cells[6]})
    if not risks:
        fail('risk register parsed to zero rows')
    return risks


def license_family(root: Path) -> str:
    text = read_text(root / 'LICENSE', 'LICENSE')
    head = ' '.join(text.split())[:200]
    if 'GNU GENERAL PUBLIC LICENSE' in head and 'Version 3' in head:
        return 'GPL-3.0'
    fail('LICENSE not recognized; refusing to guess a license claim')


def load_banned() -> list:
    lines = read_text(SITE_DIR / 'banned_phrases.txt', 'banned-phrase list')
    return [l.strip() for l in lines.splitlines()
            if l.strip() and not l.startswith('#')]


# --- derived models ----------------------------------------------------------

def exemplar_card(cfg, registry, matrix):
    rid = cfg['hero']['exemplar_requirement']
    if rid not in registry:
        fail(f'exemplar {rid} not in the requirements registry')
    if rid not in matrix:
        fail(f'exemplar {rid} not in the traceability matrix')
    row = matrix[rid]
    if not row['tests'] or not row['commits']:
        fail(f'exemplar {rid} lacks tests or commits in the matrix')
    tests = row['tests'][0] + (f' +{len(row["tests"]) - 1} more'
                               if len(row['tests']) > 1 else '')
    return {
        'id': rid,
        'title': registry[rid]['title'],
        'rows': [
            ('Spec', f'openspec/specs/{row["spec"]}/spec.md'),
            ('Tests', tests),
            ('Commits', ', '.join(row['commits'][:3])),
            ('Status', f'{row["status"]} · {row["milestone"]}'),
        ],
    }


def coverage(registry, matrix):
    live = [rid for rid, r in registry.items() if r['status'] != 'withdrawn']
    traced = [rid for rid in live if matrix.get(rid, {}).get('tests')]
    return len(traced), len(live)


def coverage_breakdown(registry, matrix):
    """Traced-test coverage split by requirement status (FRG-SITE-004)."""
    def bucket(status):
        ids = [r for r, row in registry.items() if row['status'] == status]
        tested = [r for r in ids if matrix.get(r, {}).get('tests')]
        return len(tested), len(ids)
    impl_tested, impl_total = bucket('implemented')
    v_tested, v_total = bucket('verified')
    appr_tested, appr_total = bucket('approved')
    act_tested, act_total = bucket('active')
    return {
        'implemented_tested': impl_tested + v_tested,
        'implemented_total': impl_total + v_total,
        'approved_total': appr_total,
        'active_tested': act_tested,
        'active_total': act_total,
    }


def archive_approvals(root: Path):
    """(archived change count, count with a recorded ## Approval section)."""
    archive = root / 'openspec/changes/archive'
    if not archive.is_dir():
        fail('missing source artifact: openspec change archive '
             f'({archive})')
    changes = sorted(d for d in archive.iterdir() if d.is_dir())
    if not changes:
        fail('openspec change archive is empty')
    approved = sum(1 for d in changes
                   if (d / 'proposal.md').is_file()
                   and re.search(r'^## Approval', (d / 'proposal.md').read_text(),
                                 re.M))
    return len(changes), approved


def risk_tone(status: str) -> str:
    word = status.split()[0].rstrip(':').lower()
    return {'mitigated': 'success', 'open': 'warning', 'accepted': 'info',
            'closed': 'muted', 'withdrawn': 'muted'}.get(word, 'neutral')


